#lambda function in python is used to create small anonymous functions
#like we can use lambda function to create a function that returns the square of a number
square = lambda x: x * x
print(square(5))  # Output: 25
#lambda function can also be used to find avg 
avg = lambda x, y: (x + y) / 2
print(avg(10, 20))  # Output: 15.0
