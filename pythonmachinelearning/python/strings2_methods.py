# lets say we have a string called name and we want to make it whole upper case we use the function 
#upper(), we cant change a single character in a string
name1 = input("enter your name -")
print ("your name is",name1)
print ("your name is",name1.upper())
# we can also use lower function to make the string lower case
name2= input("enter your name -")
print("your name is",name2)
print("your name is",name2.lower())
#we can use replace function to replace a part of string with another string
name3= input("enter your name -")
print("your name is",name3)
print("your name is",name3.replace("SOHAM","JOHN"))
#we can also use a function to make the first letter of the string capital
name4= input("enter your name -")
print("your name is",name4)
print("your name is",name4.capitalize())


